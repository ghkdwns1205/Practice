package Java12_29;

public class User {
	private String ID;
	private String NAME;
	private int PASS;

	public String getID() {
		return ID;
	}

	public void setID(String iD) {
		ID = iD;
	}

	public String getNAME() {
		return NAME;
	}

	public void setNAME(String nAME) {
		NAME = nAME;
	}

	public int getPASS() {
		return PASS;
	}

	public void setPASS(int pASS) {
		PASS = pASS;
	}
}
